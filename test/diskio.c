/*-----------------------------------------------------------------------*/
/* Read Sector(s)                                                        */
/*-----------------------------------------------------------------------*/

DRESULT disk_read (
	BYTE pdrv,		/* Physical drive nmuber to identify the drive */
	BYTE *buff,		/* Data buffer to store read data */
	DWORD sector,	/* Start sector in LBA */
	UINT count		/* Number of sectors to read */
)
{
	LBA0 = sector;
	LBA1 = sector >> 8;
	LBA2 = sector >> 16;
	for (count <<= 9; count--;) {
		if (STREAM_EOF) return RES_ERROR;
		*buff++ = STREAM;
	}
	return RES_OK;
}



